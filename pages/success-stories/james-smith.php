            <h1 class="mb-4">James Smith's Journey</h1>
            <h5 class="text-primary mb-3">From Corporate Executive to Leadership Coach</h5>
            
            <p>After 15 years in the corporate world, including executive roles at two Fortune 500 companies, James Smith found himself at a crossroads. "I had achieved everything I thought I wanted - the corner office, the title, the salary - but I wasn't fulfilled," James recalls. "What I truly enjoyed was mentoring junior employees and watching them grow."</p>
            
            <p>When a colleague suggested he consider coaching, James was skeptical. "I thought coaching was just for sports or life coaches talking about 'finding your bliss,'" he laughs. "I didn't realize there was a huge demand for practical, experience-based business leadership coaching."</p>
            
            <p>James started exploring EduCoach as a platform for sharing his knowledge. "I began with a simple course on managing difficult conversations in the workplace. I used real scenarios I'd encountered and practical scripts people could adapt. The response was immediate and overwhelming."</p>
            
            <h5 class="text-primary my-4">The Transformation</h5>
            
            <p>"Within six months, I had enough coaching clients and course enrollments to match my corporate salary," James explains. "What surprised me most was the diversity of my students - from startup founders to managers in completely different industries than mine who could apply my frameworks."</p>
            
            <p>James has now developed a comprehensive leadership curriculum covering everything from strategic decision-making to building inclusive teams. His most popular offering is his "Executive Presence Masterclass," which helps professionals command respect in high-stakes situations.</p>
            
            <p>"One of my proudest moments was when a client called to tell me she'd just been promoted to CEO after implementing strategies from our sessions. She said our work together gave her the confidence to pursue the role in the first place," James shares.</p>
            
            <h5 class="text-primary my-4">James's Advice for New Coaches</h5>
            
            <p>"Start with what you know deeply and authentically," James advises. "My first course succeeded because I taught from real experience, not theory. I used actual emails I'd written, meetings I'd run, and conflicts I'd resolved."</p>
            
            <p>He also emphasizes the importance of ongoing learning. "I thought I knew everything about leadership when I started coaching. I've learned more from my students than I ever did in the corporate world. Their questions and challenges push me to keep growing."</p>
            
